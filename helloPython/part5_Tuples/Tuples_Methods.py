my_tuple = (1, 2, 2, 3, 4, 2, 5)
count_of_2 = my_tuple.count(2)  # Counting occurrences of 2 in the tuple
print("Count of 2 in tuple:", count_of_2)  # Output: 3

my_tuples2 = (10, 20, 30, 40, 50)
index_of_30 = my_tuples2.index(30)  # Finding the index of 30 in the tuple
print("Index of 30 in tuple:", index_of_30)  # Output: 2


my_tuple3 = (5, 3, 8, 1, 7)

sorted_list = sorted(my_tuple3)  # Sorting the tuple and converting to a list
print("Sorted List from Tuple:", sorted_list)  # Output: [1, 3, 5, 7, 8]

my_tuples4 = (50, 30, 80, 10, 70)
length_of_tuple = len(my_tuples4)  # Getting the length of the tuple
print("Length of Tuple:", length_of_tuple)  # Output: 5